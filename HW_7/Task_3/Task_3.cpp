//Используется бесконечный цикл while, который позволяет оператору вводить команды для управления марсоходом.
//Программа запрашивает у пользователя команду и проверяет, какая команда была введена :
//W или w — движение на север(увеличение координаты Y).
//S или s — движение на юг(уменьшение координаты Y).
//A или a — движение на запад(уменьшение координаты X).
//D или d — движение на восток(увеличение координаты X).
//Q или q — выход из программы.
//Программа проверяет, не упирается ли марсоход в стену, и только тогда изменяет его позицию.
//Если введена неверная команда, программа выводит сообщение об ошибке и предлагает ввести команду снова.
//После каждого успешного перемещения программа выводит новую позицию марсохода.