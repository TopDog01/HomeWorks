#include <iostream>

int main() {

    setlocale(LC_ALL, "Russian");

    const int width = 20;  
    const int height = 15; 
    int x = width / 2;   
    int y = height / 2;  

    char command;

    
    while (true) {
        std::cout << "[Программа]: Марсоход находится на позиции " << x << ", " << y << ", введите команду (W/A/S/D для движения, Q для выхода): ";
        std::cin >> command;

        
        if (command == 'W' || command == 'w') {
            if (y < height) {
                y++;
            }
        }
        else if (command == 'S' || command == 's') {
            if (y > 1) {
                y--;
            }
        }
        else if (command == 'A' || command == 'a') {
            if (x > 1) {
                x--;
            }
        }
        else if (command == 'D' || command == 'd') {
            if (x < width) {
                x++;
            }
        }
        else if (command == 'Q' || command == 'q') {
            std::cout << "Выход из программы." << std::endl;
            break;
        }
        else {
            std::cout << "Ошибка: Неверная команда. Пожалуйста, используйте W, A, S, D для движения или Q для выхода." << std::endl;
            continue;
        }

        
        std::cout << "[Программа]: Марсоход находится на позиции " << x << ", " << y << "." << std::endl;
    }

    return 0;
}



